print("Aqui você irá digitar 3 numeros e recebera a soma dos quadrados deles como resultado")

numero1 = int(input('Digite o Primeiro numero: '))
numero2 = int(input('Digite o Segundo numero: '))
numero3 = int(input('Digite o Terceiro numero: '))

quad1 = numero1 * numero1
quad2 = numero2 * numero2
quad3 = numero3 * numero3


print(f'A soma de seus quadrados é igual a = {quad1 + quad2 + quad3}')